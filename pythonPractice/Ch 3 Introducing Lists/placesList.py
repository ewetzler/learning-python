#list 5 places to see
places = ['norway', 'spain', 'japan', 'iceland', 'peru']
#print the list
print(places)
#use sorted() to print list in alphabetical order
print(sorted(places))
#print original order of list
print(places)
#use sorted()  to print list in reverse alphabetical order
print(sorted(places, reverse=True))
#print original order of list
print(places)
#use reverse() to change the order of list. Print the list to show the order changed
places.reverse()
print(places)
#use reverse() to change the order of the list again and print to show original order
places.reverse()
print(places)
#use sort() to change list so it's sorted alphabeticaly. print the list.
places.sort()
print(places)
#use sort() to change list so lit's sorted in reverse alphabetical order, print list
places.sort(reverse=True)
